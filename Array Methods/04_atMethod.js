//at methods

let nums=[23,5,6,74,53,11,89,67];
console.log(nums[0]);
console.log(nums.at(1));

//getting last element of the array
console.log(nums[nums.length-1]);
console.log(nums.slice(-1)[0]);
console.log(nums.at(-1));

//at method also work with strings
let name="karmy";
console.log(name.at(-2));
